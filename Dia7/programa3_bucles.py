## Programa que utiliza dos tipos de ciclos, el ciclo for y while 

# Ciclo for: recorrer una lista 
frutas = ["manza", "banana", "naranja"]
for fruta in frutas: 
    print(fruta)

# while: se repiete mientras sea verdadero 
contador = 0 
while contador < 5: 
    print("Contador: ", contador)
    contador += 1

